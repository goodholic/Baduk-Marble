def style_sheet(ws, headers, data, title=None, col_widths=None):
    row = 1
    if title:
        ws.merge_cells(start_row=1, start_column=1, end_row=1, end_column=len(headers))
        c = ws.cell(row=1, column=1, value=title)
        c.font = title_font; c.fill = title_fill; c.alignment = Alignment(horizontal='center')
        row = 3
    for ci, h in enumerate(headers, 1):
        c = ws.cell(row=row, column=ci, value=h)
        c.font = header_font; c.fill = header_fill; c.alignment = Alignment(horizontal='center', wrap_text=True)
        c.border = thin_border
    row += 1
    for ri, d in enumerate(data):
        for ci, v in enumerate(d, 1):
            c = ws.cell(row=row+ri, column=ci, value=v)
            c.font = cell_font; c.border = thin_border
            c.alignment = Alignment(horizontal='center', vertical='center', wrap_text=True)
            if ri % 2 == 1: c.fill = alt_fill
    if col_widths:
        for i, w in enumerate(col_widths, 1):
            ws.column_dimensions[get_column_letter(i)].width = w

